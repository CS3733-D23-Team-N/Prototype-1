public class l1nodes {
    String nodeID;
    int xcoord;
    int ycoord;
    String floor;
    String building;
    String nodeType;
    String longName;
    String shortName;

    public l1nodes(String nodeID, int xcoord, int ycoord, String floor, String building, String nodeType, String longName, String shortName) {
        nodeID = nodeID;
        xcoord = xcoord;
        ycoord = ycoord;
        floor = floor;
        building = building;
        nodeType = nodeType;
        longName = longName;
        shortName = shortName;
    }
}
